using System;

namespace Variables
{

    class Variable
    {

        static void Main(string[] args)
        {

            System.Console.WriteLine("The is lesson Variables");

            // Variables 

            int x = 2;
            int v = 123;

            System.Console.WriteLine(x);
            System.Console.WriteLine(v);

            string str = "Hello";
            System.Console.WriteLine(str);

            char character = '$';
            System.Console.WriteLine(character);

            long l = 123456;

            System.Console.WriteLine(l);

            dynamic dy = 2345432;

            System.Console.WriteLine(dy);

            decimal dc = 123.43M;

            System.Console.WriteLine(dc);

            double summa = 23413235.655;

            System.Console.WriteLine(summa);

            float f = 21243.45F;

            System.Console.WriteLine(f);

            byte by = 123;

            System.Console.WriteLine(by);

            short sh = 2345;

            System.Console.WriteLine(sh);









        }
    }
